echo "<br>";
var_dump($x !== $z);//not identical
echo "<br>";
var_dump($x < $y);//less than
echo "<br>";
var_dump($x > $y);//greater than
echo "<br>";
var_dump($x <= $y);//less than or equal to
echo "<br>";
var_dump($x >= $y);//greater than equal to
